def find_average(num1, num2):
    """
    Returns the average of two numbers.
    
    Parameters:
        num1 (float or int): The first number.
        num2 (float or int): The second number.
    
    Returns:
        float: The average of the two numbers.
    """
    return (num1 + num2) / 2
